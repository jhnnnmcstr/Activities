// This program illustrates how integral expressions are
// evaluated.

#include <iostream>
#include <conio.h>
using namespace std;

int main()
{
	cout << "3 / 2 + 5.5 = " << 3 / 2 + 5.5 << endl;
	cout << "15.6 / 2 + 5 = " << 15.6 / 2 + 5 << endl;
	cout << "4 + 5 / 2.0 = " << 4 + 5 / 2.0 << endl;
	cout << "4 * 3 + 7 / 5 - 22.5 = " << 4 * 3 + 7 / 5 - 22.5 << endl; 
	getch();
	return 0;
}